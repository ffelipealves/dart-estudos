void main() {

  //Semelhante ao C
  double numero1 = 15;
  double numero2 = 22;

  double resultado = numero1 + numero2;
  print(resultado);

  resultado = numero1 - numero2;
  print(resultado);

  resultado = numero1 * numero2;
  print(resultado);

  resultado = numero1 / numero2;
  print(resultado);

  numero1++;
  numero2--;
  print(numero1);
  print(numero2);
}
